/*
*@brief MP2651 I2C 驱动
*/
#include "dri_i2c_mp.h"

#define RCC_I2C_MP_PORT 	RCC_APB2_PERIPH_GPIOC		

#define PORT_I2C_MP_SCL		GPIOC			
#define PIN_I2C_MP_SCL		GPIO_PIN_6		

#define PORT_I2C_MP_SDA		GPIOC			
#define PIN_I2C_MP_SDA		GPIO_PIN_7		

#define I2C_MP_SCL_PIN		GPIO_PIN_6			
#define I2C_MP_SDA_PIN		GPIO_PIN_7			

/* 定义读写SCL和SDA的宏 */
#define I2C_MP_SCL_1()  PORT_I2C_MP_SCL->PBSC = I2C_MP_SCL_PIN				/* SCL = 1 */
#define I2C_MP_SCL_0()  PORT_I2C_MP_SCL->PBC = I2C_MP_SCL_PIN				/* SCL = 0 */

#define I2C_MP_SDA_1()  PORT_I2C_MP_SDA->PBSC = I2C_MP_SDA_PIN				/* SDA = 1 */
#define I2C_MP_SDA_0()  PORT_I2C_MP_SDA->PBC = I2C_MP_SDA_PIN				/* SDA = 0 */

#define I2C_MP_SDA_READ()  ((PORT_I2C_MP_SDA->PID & I2C_MP_SDA_PIN) != 0)	/* 读SDA口线状态 */
#define I2C_MP_SCL_READ()  ((PORT_I2C_MP_SCL->PID & I2C_MP_SCL_PIN) != 0)	/* 读SCL口线状态 */

/*
*********************************************************************************************************
*	函 数 名: bsp_InitI2C_MP
*	功能说明: 配置I2C总线的GPIO，采用模拟IO的方式实现
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void bsp_InitI2C_MP(void)
{
	GPIO_InitType GPIO_InitStructure;

	RCC_EnableAPB2PeriphClk(RCC_I2C_MP_PORT, ENABLE);	/* 打开GPIO时钟 */

	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_OD;	/* 开漏输出模式 */
	
	GPIO_InitStructure.Pin = PIN_I2C_MP_SCL;
	GPIO_InitPeripheral(PORT_I2C_MP_SCL, &GPIO_InitStructure);

	GPIO_InitStructure.Pin = PIN_I2C_MP_SDA;
	GPIO_InitPeripheral(PORT_I2C_MP_SDA, &GPIO_InitStructure);

	/* 给一个停止信号, 复位I2C总线上的所有设备到待机模式 */
	i2c_Stop_MP();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Delay_MP
*	功能说明: I2C总线位延迟，最快400KHz
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
static void i2c_Delay_MP(void)
{
	uint8_t i;

	/*　
		CPU主频168MHz时，在内部Flash运行, MDK工程不优化。用台式示波器观测波形。
		循环次数为5时，SCL频率 = 1.78MHz (读耗时: 92ms, 读写正常，但是用示波器探头碰上就读写失败。时序接近临界)
		循环次数为10时，SCL频率 = 1.1MHz (读耗时: 138ms, 读速度: 118724B/s)
		循环次数为30时，SCL频率 = 440KHz， SCL高电平时间1.0us，SCL低电平时间1.2us

		上拉电阻选择2.2K欧时，SCL上升沿时间约0.5us，如果选4.7K欧，则上升沿约1us

		实际应用选择400KHz左右的速率即可
	*/
	for (i = 0; i < 30; i++);
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Start_MP
*	功能说明: CPU发起I2C总线启动信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Start_MP(void)
{
	/* 当SCL高电平时，SDA出现一个下跳沿表示I2C总线启动信号 */
	I2C_MP_SDA_1();
	I2C_MP_SCL_1();
	i2c_Delay_MP();
	I2C_MP_SDA_0();
	i2c_Delay_MP();
	
	I2C_MP_SCL_0();
	i2c_Delay_MP();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Stop_MP
*	功能说明: CPU发起I2C总线停止信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Stop_MP(void)
{
	/* 当SCL高电平时，SDA出现一个上跳沿表示I2C总线停止信号 */
	I2C_MP_SDA_0();
	I2C_MP_SCL_1();
	i2c_Delay_MP();
	I2C_MP_SDA_1();
	i2c_Delay_MP();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_SendByte_MP
*	功能说明: CPU向I2C总线设备发送8bit数据
*	形    参:  _ucByte ： 等待发送的字节
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_SendByte_MP(uint8_t _ucByte)
{
	uint8_t i;

	/* 先发送字节的高位bit7 */
	for (i = 0; i < 8; i++)
	{
		if (_ucByte & 0x80)
		{
			I2C_MP_SDA_1();
		}
		else
		{
			I2C_MP_SDA_0();
		}
		i2c_Delay_MP();
		I2C_MP_SCL_1();
		i2c_Delay_MP();
		I2C_MP_SCL_0();
		if (i == 7)
		{
			 I2C_MP_SDA_1(); // 释放总线
		}
		_ucByte <<= 1;	/* 左移一个bit */
		i2c_Delay_MP();
	}
}

/*
*********************************************************************************************************
*	函 数 名: i2c_ReadByte_MP
*	功能说明: CPU从I2C总线设备读取8bit数据
*	形    参:  无
*	返 回 值: 读到的数据
*********************************************************************************************************
*/
uint8_t i2c_ReadByte_MP(void)
{
	uint8_t i;
	uint8_t value;

	/* 读到第1个bit为数据的bit7 */
	value = 0;
	for (i = 0; i < 8; i++)
	{
		value <<= 1;
		I2C_MP_SCL_1();
		i2c_Delay_MP();
		if (I2C_MP_SDA_READ())
		{
			value++;
		}
		I2C_MP_SCL_0();
		i2c_Delay_MP();
	}
	return value;
}

/*
*********************************************************************************************************
*	函 数 名: i2c_WaitAck_MP
*	功能说明: CPU产生一个时钟，并读取器件的ACK应答信号
*	形    参:  无
*	返 回 值: 返回0表示正确应答，1表示无器件响应
*********************************************************************************************************
*/
uint8_t i2c_WaitAck_MP(void)
{
	uint8_t re;

	I2C_MP_SDA_1();	/* CPU释放SDA总线 */
	i2c_Delay_MP();
	I2C_MP_SCL_1();	/* CPU驱动SCL = 1, 此时器件会返回ACK应答 */
	i2c_Delay_MP();
	if (I2C_MP_SDA_READ())	/* CPU读取SDA口线状态 */
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	I2C_MP_SCL_0();
	i2c_Delay_MP();
	return re;
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Ack_MP
*	功能说明: CPU产生一个ACK信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_Ack_MP(void)
{
	I2C_MP_SDA_0();	/* CPU驱动SDA = 0 */
	i2c_Delay_MP();
	I2C_MP_SCL_1();	/* CPU产生1个时钟 */
	i2c_Delay_MP();
	I2C_MP_SCL_0();
	i2c_Delay_MP();
	I2C_MP_SDA_1();	/* CPU释放SDA总线 */
}

/*
*********************************************************************************************************
*	函 数 名: i2c_NAck_MP
*	功能说明: CPU产生1个NACK信号
*	形    参:  无
*	返 回 值: 无
*********************************************************************************************************
*/
void i2c_NAck_MP(void)
{
	I2C_MP_SDA_1();	/* CPU驱动SDA = 1 */
	i2c_Delay_MP();
	I2C_MP_SCL_1();	/* CPU产生1个时钟 */
	i2c_Delay_MP();
	I2C_MP_SCL_0();
	i2c_Delay_MP();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_CheckDevice_MP
*	功能说明: 检测I2C总线设备，CPU向发送设备地址，然后读取设备应答来判断该设备是否存在
*	形    参:  _Address：设备的I2C总线地址
*	返 回 值: 返回值 0 表示正确， 返回1表示未探测到
*********************************************************************************************************
*/
uint8_t i2c_CheckDevice_MP(uint8_t _Address)
{
	uint8_t ucAck;

	if (I2C_MP_SDA_READ() && I2C_MP_SCL_READ())
	{
		i2c_Start_MP();		/* 发送启动信号 */

		/* 发送设备地址+读写控制bit（0 = w， 1 = r) bit7 先传 */
		i2c_SendByte_MP(_Address | I2C_MP_WR);
		ucAck = i2c_WaitAck_MP();	/* 检测设备的ACK应答 */

		i2c_Stop_MP();			/* 发送停止信号 */

		return ucAck;
	}
	return 1;	/* I2C总线异常 */
}





